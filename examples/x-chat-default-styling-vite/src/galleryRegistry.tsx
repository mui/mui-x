export type GallerySectionId =
  | 'full-surfaces'
  | 'layout-and-navigation'
  | 'messages'
  | 'composer'
  | 'empty-loading-stream'
  | 'ai-rich-content';

export type GalleryEntryStatus = 'core' | 'compound' | 'slot' | 'state' | 'presentational';

export interface GalleryComponentEntry {
  id: string;
  title: string;
  importName: string;
  sectionId: GallerySectionId;
  sectionTitle: string;
  description: string;
  status: GalleryEntryStatus;
  parent?: string;
  slots?: string[];
  classKeys?: string[];
  docsPath?: string;
  sourcePath: string;
  playground: React.ReactNode;
  codeExample?: string;
  /** Optional inline-SVG schematic; falls back to PNG when omitted. */
  Thumbnail?: React.ComponentType;
}

export interface GallerySection {
  id: GallerySectionId;
  title: string;
  description: string;
  entries: GalleryComponentEntry[];
}

export interface GallerySearchItem {
  id: string;
  title: string;
  section: string;
  status: GalleryEntryStatus | 'section';
  description?: string;
  keywords: string[];
}

