function entry(definition: Omit<GalleryComponentEntry, 'sectionTitle'>): GalleryComponentEntry {
  const section = sectionInfo.find((item) => item.id === definition.sectionId);
  if (!section) {
    throw new Error(`Unknown gallery section: ${definition.sectionId}`);
  }
  return { ...definition, sectionTitle: section.title };
}

