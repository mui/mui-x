import * as fs from 'fs/promises';
import * as path from 'path';
import * as url from 'url';

const currentDirectory = url.fileURLToPath(new URL('.', import.meta.url));
const docsRoot = path.resolve(currentDirectory, '..');
const mainDemoRoot = path.resolve(docsRoot, 'src/modules/components/overview/chat/mainDemo');

const sourceFiles = {
  basicSource: path.join(mainDemoRoot, 'code/basic/App.tsx'),
  messengerSource: path.join(mainDemoRoot, 'code/messenger/App.tsx'),
  agentSource: path.join(mainDemoRoot, 'code/agent/App.tsx'),
  widgetSource: path.join(mainDemoRoot, 'code/widget/App.tsx'),
  captionsSource: path.join(mainDemoRoot, 'code/captions/App.tsx'),
  copilotSource: path.join(mainDemoRoot, 'code/copilot/App.tsx'),
};

const outputPath = path.join(mainDemoRoot, 'generatedCodeSources.ts');

async function buildOutput() {
  const entries = await Promise.all(
    Object.entries(sourceFiles).map(async ([exportName, filePath]) => {
      const content = await fs.readFile(filePath, 'utf8');
      const relativePath = path.relative(docsRoot, filePath).replaceAll(path.sep, '/');

      return { exportName, relativePath, content };
    }),
  );

  const body = [
    '// Generated by `node docs/scripts/generateChatOverviewCodeSources.mjs`.',
    '// Do not edit this file directly; update the matching source file instead.',
    '',
    ...entries.flatMap((entry) => [
      `// Source: ${entry.relativePath}`,
      `export const ${entry.exportName} =`,
      `  ${JSON.stringify(entry.content)};`,
      '',
    ]),
  ]
    .join('\n')
    .trimEnd();

  return `${body}\n`;
}

async function main() {
  // `--check` regenerates in-memory and fails if the committed artifact drifted
  // from its source files (so CI can guard against forgetting to regenerate),
  // without writing anything.
  const check = process.argv.includes('--check');
  const output = await buildOutput();

  if (!check) {
    await fs.writeFile(outputPath, output, 'utf8');
    return;
  }

  const current = await fs.readFile(outputPath, 'utf8').catch(() => '');
  if (current !== output) {
    const relativeOutput = path.relative(docsRoot, outputPath).replaceAll(path.sep, '/');
    console.error(
      `${relativeOutput} is out of date. Run ` +
        '`node docs/scripts/generateChatOverviewCodeSources.mjs` and commit the result.',
    );
    process.exitCode = 1;
  }
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
