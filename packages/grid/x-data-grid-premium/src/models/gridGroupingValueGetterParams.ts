import {
  GridRowId,
  GridRowModel,
  GridValidRowModel,
  GridGroupNode,
  GridColDef,
} from '@mui/x-data-grid-pro';

/**
 * Parameters passed to `colDef.groupingValueGetter`.
 */
export interface GridGroupingValueGetterParams<R extends GridValidRowModel = any, V = any> {
  /**
   * The grid row id.
   */
  id: GridRowId;
  /**
   * The column field of the cell that triggered the event.
   */
  field: string;
  /**
   * The cell value, does not take `valueGetter` into account.
   */
  value: V;
  /**
   * The row model of the row that the current cell belongs to.
   */
  row: GridRowModel<R>;
  /**
   * The column of the row that the current cell belongs to.
   */
  colDef: GridColDef<R, V, any>;
  /**
   * The node of the row that the current cell belongs to.
   * It only contains the information available before the actual grouping.
   */
  rowNode: Pick<GridGroupNode, 'id' | 'isAutoGenerated'>;
}
