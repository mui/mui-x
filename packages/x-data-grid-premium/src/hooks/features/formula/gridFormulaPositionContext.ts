import type { RefObject } from '@mui/x-internals/types';
import {
  GRID_CHECKBOX_SELECTION_FIELD,
  GRID_DETAIL_PANEL_TOGGLE_FIELD,
  GRID_REORDER_COL_DEF,
  gridDataRowIdsSelector,
  gridFilteredSortedRowIdsSelector,
  gridRowTreeSelector,
  gridSortedRowIdsSelector,
  gridVisibleColumnFieldsSelector,
} from '@mui/x-data-grid-pro';
import type { GridRowId, GridRowTreeConfig } from '@mui/x-data-grid-pro';
import { GRID_TREE_DATA_GROUPING_FIELD, isGroupingColumn } from '@mui/x-data-grid-pro/internals';
import type { GridPrivateApiPremium } from '../../../models/gridApiPremium';
import type { GridStatePremium } from '../../../models/gridStatePremium';
import type { FormulaPositionContext } from './engine';

/**
 * The inputs a position context is built from: sorted + filtered data-row
 * order and visible-column order. Rebind events compare snapshots to skip
 * rebinding when nothing actually moved.
 */
export interface GridFormulaPositionSnapshot {
  rowIds: GridRowId[];
  fields: string[];
}

/**
 * Only data rows take part in the position context: autogenerated grouping
 * nodes, footers, skeleton and pinned rows are excluded. Tree-data parents
 * are real data rows and keep a position.
 */
function collectPositionRowIds(
  sourceIds: readonly GridRowId[],
  tree: GridRowTreeConfig,
): GridRowId[] {
  const rowIds: GridRowId[] = [];
  for (const id of sourceIds) {
    const node = tree[id];
    if (
      node !== undefined &&
      (node.type === 'leaf' || (node.type === 'group' && !node.isAutoGenerated))
    ) {
      rowIds.push(id);
    }
  }
  return rowIds;
}

const UTILITY_FIELDS = new Set<string>([
  GRID_CHECKBOX_SELECTION_FIELD,
  GRID_DETAIL_PANEL_TOGGLE_FIELD,
  GRID_REORDER_COL_DEF.field,
  GRID_TREE_DATA_GROUPING_FIELD,
]);

/**
 * Mirror of the row-side exclusion on the column axis: utility columns the
 * grid generates (selection checkbox, detail panel toggle, row reorder
 * handle, grouping columns) hold no data and take no column position —
 * otherwise enabling `checkboxSelection` would shift every positional
 * column reference by one.
 */
function isPositionedDataField(field: string): boolean {
  return !UTILITY_FIELDS.has(field) && !isGroupingColumn(field);
}

export function buildFormulaPositionSnapshot(
  apiRef: RefObject<GridPrivateApiPremium>,
): GridFormulaPositionSnapshot {
  const dataRowIds = gridDataRowIdsSelector(apiRef);
  // Mount window: filtering applies before the first `applySorting`, so
  // `sortedRows` can be empty while rows exist. Fall back to the row-tree
  // order the initial evaluation used — the snapshots compare equal and the
  // rebind waits for the mount `sortedRowsSet` instead of binding everything
  // against an empty view.
  const sourceIds =
    gridSortedRowIdsSelector(apiRef).length === 0 && dataRowIds.length > 0
      ? dataRowIds
      : gridFilteredSortedRowIdsSelector(apiRef);
  return {
    rowIds: collectPositionRowIds(sourceIds, gridRowTreeSelector(apiRef)),
    fields: gridVisibleColumnFieldsSelector(apiRef).filter(isPositionedDataField),
  };
}

/**
 * State-based variant for the initial evaluation: the sorting and filtering
 * states initialize after the formula state, so the initial snapshot uses
 * the row-tree order. The mount-time sorting/filtering cascade publishes
 * `sortedRowsSet`/`filteredRowsSet`, which rebind position-dependent
 * formulas against the real view order.
 */
export function buildFormulaPositionSnapshotFromState(
  state: Partial<GridStatePremium>,
): GridFormulaPositionSnapshot {
  const visibilityModel = state.columns?.columnVisibilityModel ?? {};
  return {
    rowIds: state.rows ? collectPositionRowIds(state.rows.dataRowIds, state.rows.tree) : [],
    fields: (state.columns?.orderedFields ?? []).filter(
      (field) => visibilityModel[field] !== false && isPositionedDataField(field),
    ),
  };
}

export function createFormulaPositionContext(
  snapshot: GridFormulaPositionSnapshot,
  version: number,
): FormulaPositionContext {
  const { rowIds, fields } = snapshot;
  // The reverse map keys by stringified id, consistent with the engine
  // cell-key format and the grid's rows lookup — both coerce ids to strings.
  const rowIdToPosition = new Map<string, number>();
  rowIds.forEach((id, index) => rowIdToPosition.set(String(id), index + 1));
  const fieldToPosition = new Map<string, number>();
  fields.forEach((field, index) => fieldToPosition.set(field, index + 1));
  return {
    version,
    rowCount: rowIds.length,
    columnCount: fields.length,
    getRowIdAtPosition: (index) => rowIds[index - 1],
    getPositionOfRowId: (id) => rowIdToPosition.get(String(id)),
    getFieldAtPosition: (index) => fields[index - 1],
    getPositionOfField: (field) => fieldToPosition.get(field),
  };
}

export function arePositionArraysEqual<T>(a: readonly T[], b: readonly T[]): boolean {
  if (a.length !== b.length) {
    return false;
  }
  for (let i = 0; i < a.length; i += 1) {
    if (a[i] !== b[i]) {
      return false;
    }
  }
  return true;
}
